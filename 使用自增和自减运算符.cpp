#include <iostream>
using namespace std;
int main()
{
   int a;

   a=4;
   cout << a << endl;
   cout << a++ << endl;
   cout << a << endl;
   cout << endl;

   a=4;
   cout << a << endl;
   cout << ++a << endl;
   cout << a << endl;
   cout << endl;

   a=4;
   cout << a << endl;
   cout << a-- << endl;
   cout << a << endl;
   cout << endl;

   a=4;
   cout << a << endl;
   cout << --a << endl;
   cout << a << endl;

}
